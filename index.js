function add(a, b) {
  var x = a + b 
  return x
}

function subtract(a, b) {
  var x = a - b 
  return x
}

function multiply(a, b) {
  var x = a * b 
  return x
}

function divide(a, b) {
  var x = a / b 
  return x
}

function inc(n) {
  var x = 1
  x += n
  return x
}

function dec(n) {

  x -= n
  return x
}

